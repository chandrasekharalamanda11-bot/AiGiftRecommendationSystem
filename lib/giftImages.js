/**
 * Resolves the best curated Unsplash image for any given gift recommendation
 */
export function resolveGiftImage(name = "", category = "") {
  const normName = name.toLowerCase().trim();
  const normCat = category.toLowerCase().trim();

  // 1. Direct match on predefined sample gifts
  if (PREDEFINED_GIFT_IMAGES[normName]) {
    return PREDEFINED_GIFT_IMAGES[normName];
  }

  // 2. Scan predefined lists for partial string matches
  for (const [key, value] of Object.entries(PREDEFINED_GIFT_IMAGES)) {
    if (normName.includes(key) || key.includes(normName)) {
      return value;
    }
  }

  // 3. Scan keyword patterns
  for (const item of KEYWORD_GIFT_IMAGES) {
    if (item.keywords.some(kw => normName.includes(kw))) {
      return item.url;
    }
  }

  // 4. Fallback to category-based matching
  for (const [catKey, catImg] of Object.entries(CATEGORY_FALLBACK_IMAGES)) {
    if (normCat.includes(catKey) || normName.includes(catKey)) {
      return catImg;
    }
  }

  // 5. Hard default
  return CATEGORY_FALLBACK_IMAGES.general;
}
