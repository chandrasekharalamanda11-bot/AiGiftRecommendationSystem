/**
 * Filter and score the sample gifts based on user selections.
 * Returns 3-4 highly relevant gifts.
 */
export function getFallbackRecommendations({ relationship, age, occasion, interests = "", budget, vibe = [] }) {
  // Extract and normalize query interests
  const normalizedInterests = interests
    .toLowerCase()
    .split(/[\s,]+/)
    .map(i => i.trim())
    .filter(i => i.length > 1);

  // Expand query interests using synonym mapping
  const expandedInterests = [...normalizedInterests];
  normalizedInterests.forEach(interest => {
    Object.keys(SYNONYM_MAP).forEach(key => {
      if (key.includes(interest) || interest.includes(key)) {
        expandedInterests.push(key, ...SYNONYM_MAP[key]);
      }
    });
  });
  
  // Deduplicate expanded interests
  const uniqueQueryInterests = Array.from(new Set(expandedInterests));
  const parsedVibes = Array.isArray(vibe) ? vibe : [vibe].filter(Boolean);
  const budgetOrder = ["Under ₹1,000", "₹1,000 - ₹2,500", "₹2,500 - ₹5,000", "₹5,000 - ₹10,000", "₹10,000+"];

  // Step 1: Strict Age compatibility check
  let candidates = SAMPLE_GIFTS.filter(gift => {
    return gift.suitableAges.some(a => a.toLowerCase() === age.toLowerCase());
  });

  // Step 2: Strict Budget limit check (exclude items strictly above budget tier)
  let strictBudgetCandidates = candidates.filter(gift => {
    const giftIdx = budgetOrder.indexOf(gift.priceRange);
    const userIdx = budgetOrder.indexOf(budget);
    if (giftIdx !== -1 && userIdx !== -1 && giftIdx > userIdx) {
      return false; // strictly over budget -> exclude
    }
    return true;
  });

  // Safe fallback if strict budget filtering returns fewer than 3 options
  if (strictBudgetCandidates.length >= 3) {
    candidates = strictBudgetCandidates;
  } else {
    // Relax budget check to allow at most 1 tier higher as a fallback
    let relaxedBudgetCandidates = candidates.filter(gift => {
      const giftIdx = budgetOrder.indexOf(gift.priceRange);
      const userIdx = budgetOrder.indexOf(budget);
      if (giftIdx !== -1 && userIdx !== -1 && giftIdx > userIdx + 1) {
        return false; // strictly more than 1 tier over budget -> exclude
      }
      return true;
    });
    if (relaxedBudgetCandidates.length >= 3) {
      candidates = relaxedBudgetCandidates;
    }
    // Otherwise keep candidates as just filtered by age
  }

  // Step 3: Score the candidates
  const scoredGifts = candidates.map(gift => {
    let score = 0;

    // 1. Budget Proximity/Matching (Score range: 0 to 15)
    if (gift.priceRange === budget) {
      score += 15;
    } else {
      const giftIdx = budgetOrder.indexOf(gift.priceRange);
      const userIdx = budgetOrder.indexOf(budget);
      if (giftIdx !== -1 && userIdx !== -1) {
        const diff = giftIdx - userIdx;
        if (diff === 1) {
          score -= 10; // Slightly over budget (penalize slightly)
        } else if (diff === -1) {
          score += 5;  // Under budget is proximity bonus
        } else if (diff < -1) {
          score += 2;  // Safe under budget option
        }
      }
    }

    // 2. Age Suitability (Score range: +25)
    if (gift.suitableAges.some(a => a.toLowerCase() === age.toLowerCase())) {
      score += 25;
    }

    // 3. Relationship Match (Score range: 0 to 15)
    if (gift.suitableRelationships.some(r => r.toLowerCase() === relationship.toLowerCase())) {
      score += 15;
    }

    // 4. Occasion Match (Score range: 0 to 20)
    if (gift.suitableOccasions.some(o => o.toLowerCase() === occasion.toLowerCase())) {
      score += 20;
    }

    // 5. Vibe Match (Score range: 0 to 24)
    parsedVibes.forEach(userVibe => {
      if (gift.vibes.some(v => v.toLowerCase() === userVibe.toLowerCase())) {
        score += 8;
      }
    });

    // 6. Interest Match (High weight boost: +30 per unique match)
    uniqueQueryInterests.forEach(interest => {
      // Direct tag matches
      if (gift.interests.some(giftInt => giftInt.toLowerCase().includes(interest) || interest.includes(giftInt.toLowerCase()))) {
        score += 30;
      }
      // Content-based keyword search
      if (gift.name.toLowerCase().includes(interest) || gift.description.toLowerCase().includes(interest)) {
        score += 25;
      }
    });

    return { ...gift, score };
  });

  // Sort candidates by score descending
  scoredGifts.sort((a, b) => b.score - a.score);

  // Take the top 5 candidates as the high-matching pool to select from
  const poolSize = Math.min(5, scoredGifts.length);
  const topPool = scoredGifts.slice(0, poolSize);

  // Randomly shuffle the top pool to ensure variety when refreshed
  const shuffledPool = [...topPool].sort(() => Math.random() - 0.5);

  // Return the first 3 items from the shuffled pool
  const selected = shuffledPool.slice(0, 3).map(gift => ({
    id: gift.id,
    name: gift.name,
    description: gift.description,
    estimatedPrice: gift.estimatedPrice,
    whyItFits: gift.whyItFits,
    category: gift.category,
    sparkOption: gift.sparkOption
  }));

  return selected;
}
